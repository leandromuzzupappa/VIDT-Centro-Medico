				<h3>Desarrollo y Convocatoria</h3>

<p>Desde los comienzos de sus actividades <strong><span>VIDT Oncología Radiante</span></strong> desarrolla una asistencia
social orientada a la atención de <strong>pacientes</strong> oncológicos <strong>sin</strong> ningún tipo de <strong>cobertura</strong>
