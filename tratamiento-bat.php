			<p class="big"><strong><span>La braquiterapia</span> es un método para suministrar radiación  mediante la colocación de fuentes radiactivas dentro o inmediatamente adyacentes al tejido tumoral. </strong></p>
			
<p>Una característica clave de la braquiterapia es que la irradiación afecta sólo a un área muy localizada alrededor de las fuentes de radiación. Por lo tanto, se reduce la exposición a los tejidos sanos más alejados de las fuentes.
La braquiterapia utiliza técnicas de alta tasa dosis (HDR), y se puede administrar sola o en combinación con otras terapias como cirugía, radioterapia externa (EBRT) y quimioterapia.</p>

<p>Durante la braquiterapia HDR, una sola fuente radiactiva se coloca temporalmente en el interior del sitio tumoral durante unos minutos y luego se retira.</p>

<quote>La fuente radioactiva viaja dentro de pequeños catéteres controlados por un dispositivo remoto de acuerdo a la planificación computarizada previa.</quote>

<p>Dado que la posición de la fuente puede ser ajustada con precisión, podemos crear distribuciones de dosis personalizadas para satisfacer las necesidades de cada paciente, por lo cual, los tumores pueden ser tratados con dosis muy altas de radiación localizada mientras se reducen en gran medida las dosis a los tejidos sanos circundantes. 
La braquiterapia HDR es usada comúnmente para tratar tumores de próstata, ginecológicos, de cabeza y cuello, entre otras neoplasias malignas.</p>

<quote>La ventaja de la braquiterapia de alta dosis, es que se trata de un tratamiento ambulatorio, en pocas sesiones, seguro e indoloro, con un desarrollo confortable</quote>

<p><strong>Después de la aplicación de breves minutos, el paciente puede retomar sus actividades habituales.</strong></p>

				
			
				
