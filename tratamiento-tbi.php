 	<p class="big">Este tratamiento consiste en <strong>irradiar el volumen corporal del paciente en forma homogénea
con Fotones de energía adecuada</strong>, lo cual no puede lograrse con un conjunto de campos
convencionales de Fotones, por problemas de superposición y empalme de campos
múltiples contiguos ya que de ellos resultarían distribuciones irregulares de Isodosis y zonas
subdosadas clínicamente inaceptables.</p><br>
<br>


 	<h3>El Tratamiento comprende:</h3>

 <p>	<strong>1 - Planificación de tratamiento desde su aspecto físico-radioterapéutico o sea:</strong></p>


<ul>
 <li>Ajuste y Calibración del Haz básico de tratamiento de 6 MeV de Fotones con
	 sistema Dosimétrico Computarizado y dos (2) Dosímetros calibrados.</li>
	<li>Preparación de camilla regulable y elementos de fijación del paciente.</li>
<li>Estudio Dosimétrico completo de la distribución espacial de dosis por medio
	 de Films radiográficos especiales con Fotodensitómetro .</li>
	<li>Preparación de fantomas de acrílico Simuladores de la anatomía del paciente.</li>
<li>Calibración del Haz de radiación a la distancia especial del tratamiento con
	 Dosímetros Calibrados y Dosímetros Termoluminiscentes ( TLD ).</li>

 </ul>
	<p><strong>2 - Elaboración de las protecciones Físicas del paciente (para blindar regiones que no deben
     ser irradiadas)</strong></p>

<p><strong>3 - Dosimetría completa del tratamiento.</strong></p>

<p><strong>4 - Seguimiento clínico del paciente.</strong></p>

<p><strong>5 - Monitoreo y control de los parámetros de funcionamiento del Acelerador Lineal para
     garantizar y corroborar el tratamiento previamente planificado.</strong></p>
<ul>
 	<li>La técnica que hemos implementado en nuestro Acelerador Lineal Clinac 6/100,
     consiste en tratar al paciente a una distancia extendida de 4 metros con 2 campos
     antero-posteriores para cubrir en forma completa todo el volúmen corporal, con
		excepción de los órganos a proteger.</li>

<li>Para este desarrollo, nuestros físicos emplearon un Sistema Dosimétrico
     Computarizado, único en el país, para efectuar las mediciones necesarias para
     caracterizar físicamente los haces de radiación empleados. Dicha computadora
     maneja en forma automática dos (2) detectores de radiación dentro de un Fantoma
		líquido.</li>

<li>El equipo de trabajo que interviene en este tratamiento incluye: Un especialista en
     Física de la Radioterapia (C.N.E.A.), quien habiendo calibrado el Acelerador para las
     condiciones de trabajo, controla y supervisa la concreción del tratamiento de acuerdo
     con lo previamente planificado y diseñado, un Equipo de Médicos que habiendo
     evaluado y estadificado al paciente, controlan su evolución clínica y adaptan la
     prescripción a cada caso individual, el Ingeniero Electrónico responsable del
     mantenimiento del Acelerador quien va monitoreando el funcionamiento adecuado de
     equipo con instrumental apropiado (Osciloscopio y Multimetros Digitales), técnicos
		operadores del Acelerador y Personal Auxiliar.</li>
