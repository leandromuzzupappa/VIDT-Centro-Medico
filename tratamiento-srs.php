		<p class="big"><strong><span>La Radiocirugía craneal (stereatactic radio surgery)</span> es un método no quirúrgico que permite la administración de una dosis de rayos X centrada precisamente en un blanco tumoral o funcional dentro del cerebro.</strong><br>
<br>
<br>
El uso de métodos estereotáxicos permite administrar en forma segura y milimétrica, una dosis ablativa en el sitio tumoral disminuyendo significativamente la dosis en el tejido cerebral en el área inmediata de la región. </p>

<quote>El resultado es una mayor capacidad para controlar la enfermedad intracraneal junto con una reducción en el riesgo de efectos secundarios de la radioterapia. </quote>

<p>La Radiocirugía puede ser realizada en una sola sesión, llamada Radiocirugía Estereotáxica (SRS), si el tumor tiene un diámetro menor a 3 cm y  para tumores más voluminosos, como una serie de tratamientos diarios, denominada Radioterapia Estereotáxica (SRT). </p>

<p>Esta práctica es ambulatoria, no conlleva dolor, riesgo de sangrado, infecciones ni anestesia. 
SRS y SRT se utilizan para una variedad de tumores cerebrales.</p>

<h3>Patologías susceptibles a ser tratadas en Radiocirugía:</h3>
<ul>
				<li>Malformaciones Arterio-Venosas.</li>
				<li>Gliomas recidivados </li>
				<li>Metástasis Cerebrales</li>
				<li>Neurinoma de 8º Par (Uni o Bilateral)</li>
				<li>Tumores de Hipófisis: Funcionantes y no Funcionantes.</li>
				<li>Craneofaringeomas Sólidos.</li>
				<li>Germinomas.</li>
				<li>Gangliocitoma y Ganglioneuroma.</li>
				<li>Cordomas. </li>
