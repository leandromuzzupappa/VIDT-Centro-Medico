		<p class="big"><strong><span>La Radioterapia Estereotáctica Extra-Craneal (SBRT)</span> es una técnica especializada para tumores situados fuera del cerebro.</strong> <br>
<br>
Permite utilizar haces de radiación extremadamente focalizados y dosis ablativas (dosis altas en tiempo corto) de radiación mediante la combinación de sofisticados recursos, que incluyen una inmovilización muy rigurosa del cuerpo y técnicas avanzadas de imágenes que permitan al haz de radiación seguir el movimiento del órgano durante el tratamiento.</p>
		
<p><strong>La SBRT tiene la ventaja de ser un tratamiento que se administra en pocas sesiones</strong> (normalmente de tres a cinco), no requiriendo sedación ni anestesia y permitiendo que la radiación sea administrada de una manera más segura y eficaz que otras técnicas para las siguientes patologías:</p>

<ul>
	<li>Tumores de pulmón localizados primitivos o metastasicos,</li>
	<li>Prostáticos de riesgo bajo o intermedio, </li>
	<li>Primarios o secundarios hepáticos, </li>
	<li>Vertebrales, </li>
	<li>Pancreáticos, </li>
	<li>Metástasis ganglionares, </li>
<li>Permitiendo además re-irradiaciones y tratamiento efectivo de la enfermedad oligo-metastásica.</li>
</ul>
<p><strong>Debido a que el haz es tan preciso, se puede evitar aún más la  dosis en tejidos sanos circundantes, lo que limita los efectos secundarios y complicaciones potenciales de la radiación.</strong></p>

<quote>El tratamiento es completado en 1 a dos semanas y de forma ambulatoria.</quote>



				
				
