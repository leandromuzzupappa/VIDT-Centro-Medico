		<p class="big"><strong><span>La terapia de radiación conformacional tridimensionada, o RT 3D</span>, utiliza computadoras y técnicas especiales de imágenes, tal  como: tomografía computada, resonancia magnética o PET para determinar el tamaño, la forma y la localización del tumor, así como los órganos circundantes.</strong>  Dado que los tumores no son regulares y se presentan de  diferentes formas y tamaños, con la técnica 3D, el plan de tratamiento de radiación se adapta específicamente a la anatomía de cada paciente. </p>
		
<p>Al comenzar el proceso de planificación,  se realiza la tomografía computada de la parte del cuerpo que se va a  tratar.
Estas imágenes se cargan en un ordenador con software especializado y se utilizan para crear el plan de tratamiento de radioterapia, el cual está indicado y supervisado por el radio-oncólogo.</p>
<p><strong>Esta planificación tiene el objetivo de permitir la entrega de campos de radiación que se adapten al tamaño, la forma y los contornos del órgano afectado.</strong> </p>

<p>Debido a que los haces de radiación son dirigidos con precisión, el tejido sano cercano recibe menor dosis de radiación, evitándose, en gran medida, secuelas por el tratamiento</p>

		
		
