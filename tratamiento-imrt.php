			<p class="big"><strong><span>La radioterapia de intensidad modulada, o IMRT</span>, se considera una de las técnicas de radioterapia con haz de fotones más avanzada para destruir las células tumorales.</strong></p>
			
<p>Este método de terapia Radiante tiene como objetivo entregar dosis más altas de radiación al sitio tumoral, minimizando el daño a los órganos vitales circundantes, con mayor conformación y  precisión que la Radioterapia Tridimensionada.
Utilizando el hecho de que no todos los tejidos reaccionan a la radiación de la misma manera, la IMRT varía la intensidad del haz, ya que permite dividir al mismo, en cientos de pequeños haces independientes e irradia al paciente desde ángulos y ubicaciones más favorables.</p>

<quote>Este tipo de tratamiento se ha convertido en un estándar para los pacientes donde su enfermedad se localiza próxima a estructuras sanas.</quote>

<p>Durante el proceso de planificación del tratamiento de IMRT, un robusto sistema de planificación adquiere información sobre la ubicación tumoral y sus estructuras circundantes a partir de las imágenes de tomografía computada del paciente. 
La computadora mapea los ángulos y ubicaciones óptimas, y se asignan las intensidades de haz más altas al sitio a tratar, permitiendo la integridad y funcionalidad de sitios vecinos.</p>

<quote>Una vez que se genera el plan de tratamiento, el ordenador lo envía al acelerador linear para realizar dicho el tratamiento, en forma ambulatoria y no dolorosa.</quote>

	
