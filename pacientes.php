<li><strong><span>¿En qué consiste la Simulación?:</span></strong>  La Simulación es un <strong>estudio preparatorio del tratamiento que Ud. recibirá.</strong> Se efectúa al inicio del mismo. Una Técnica Especialista en Radioterapia lo recibirá y ayudará a acostarse en la camilla de un equipo especial denominado Simulador. <strong>El médico y el técnico Radioterapeuta definirán exactamente el área de tratamiento que se verá en un monitor de televisión.</strong> Se le tomarán algunas medidas, se le hará un pequeño tatuaje
