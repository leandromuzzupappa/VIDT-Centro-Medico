 	<h3>Conceptos</h3>

 	<p>En VIDT Oncología Radiante ofrecemos alternativas de tratamiento con la más avanzada tecnología en radioterapia como es la radiocirugía estereotáctica cerebral (SRS/RC).</p>
<p>La radiocirugía estereotáctica  es un tipo de radiación que combina el exacto posicionamiento del paciente mediante un sistema de inmovilización sin marco (frameless) y una precisa determinación del tumor, guiada por imágenes (IGRT).
Debido a su precisión milimétrica la RC puede entregar altas dosis de radiación en pocas sesiones, con menor riesgo de los tejidos sanos circundantes a la lesión.
El tratamiento se entrega en una a cinco fracciones y pueden tratarse más de una lesión en forma simultánea. Cuando se utilizan más de una fracción también se conoce como Radioterapia Estereotáctica Fraccionada.</p><br>



 	<h3>¿Cómo es el procedimiento?</h3>

 	<p>Una vez que se diseña la inmovilización del paciente, se obtienen imágenes tridimensionales (Tomografía Computada) que se fusionan con las imágenes previas (Resonancia Nuclear Magnética) para lograr una precisa localización y que nos provee información del tamaño y la forma de la lesión.
El equipo tratante, conformado por Médicos Radioterapeutas,  Físico médico, Ingenieros y Técnico en radioterapia, utiliza las imágenes para generar el plan de tratamiento y determinar las dosis de radiación necesarias para cada caso.
En cada sesión, el Técnico, lo posicionará cuidadosamente en la camilla del Acelerador Lineal (LINAC) y le colocará el sistema de inmovilización personalizado. El médico verificará las imágenes obtenidas previas a la entrega del tratamiento y supervisará todo el proceso.</p>
<br>
				
 	<h3>¿En quiénes se puede utilizar?</h3>

 	<p>La radiocirugía es efectiva frente a tumores pequeños, bien delimitados, inoperables o en localizaciones de alto riesgo quirúrgico como el cerebro o la columna vertebral.
Es una opción en el manejo de las metástasis, gliomas recidivantes, tumores de base de cráneo, meningiomas, schwanomas, adenomas de hipófisis, malformaciones arteriovenosas (MAV) y neuralgia del trigémino, entre otras.  
</p>
<br>


