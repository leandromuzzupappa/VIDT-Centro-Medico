			<p class="big"><strong><span>La Radioterapia Guiada por Imágenes (IGRT)</span>  es una de las innovaciones más avanzadas en la tecnología para tratamientos oncológicos disponibles.</strong></p> 
			
<p>IGRT se refiere al uso de imágenes avanzadas para asegurar que el posicionamiento del tumor coincidirá con el suministro de dosis altamente conformada que solo puede lograrse en Aceleradores Lineales de vanguardia.
Existen muchos factores que pueden contribuir a diferencias entre la de dosis planeada y de dosis que se va a administrar al paciente, dado que las estructuras se mueven durante la respiración o movimientos fisiológicos del organismo.
En la IGRT se toman imágenes día a día y de forma dinámica, a fin de definir como es y donde se encuentra el volumen blanco o tumoral de cada paciente durante las semanas que dura el tratamiento, lo cual ofrece una mayor precisión a la hora de realizar la irradiación. </p>

<p>Normalmente, la posición del tumor puede variar entre sesiones de tratamiento debido a los movimientos internos del cuerpo (respiración, movimientos intestinales, etc.). Para compensar esta falta de precisión, se considera un margen de "seguridad" alrededor del tumor, de modo que que el tumor esté siempre incluido en la zona irradiada. </p>

<quote>VIDT Oncología Radiante utiliza un sistema IGRT multidimensional de tecnología de punta, denominado  "Cone Beam CT" (CBCT), que da imágenes volumétricas del paciente, equivalentes a las del scanner de planificación, permitiendo de esta forma un mejor ajuste al  volumen de irradiación. </quote>
			
			
<h3>Ventajas de IGRT:</h3>

<ul>
<li>Menor irradiación de estructuras vecinas y mayor precisión tumoral.</li>
<li>Control diario de posibles variaciones anatómicas en zonas de tratamiento (Tumor, regiones ganglionares, órganos sanos, etc.).</li>
<li>Obtención y registro de posicionamiento y ejecución de tratamiento de manera diaria.</li>
<li>Posibilidad de irradiación de tumores en órganos en movimiento (pulmón, próstata, etc.)</li>
<li>Corrección automática del posicionamiento del paciente.</li>
<li>Registrar imágenes deformables, re planificar nuevos tratamientos, imagen en tiempo real y cálculo de dosis acumulada.</li>
</ul>

			

				
				
